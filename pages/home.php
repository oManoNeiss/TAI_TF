                <h2>Ozzy Osbourne</h2>
                <p>
                    John Michael Osbourne, conhecido como Ozzy Osbourne (Aston, Inglaterra, 3 de dezembro de 1948), é um
                    músico, compositor e vocalista britânico.[1] Famoso tanto por seu trabalho como vocalista da banda
                    britânica Black Sabbath, como por sua carreira solo. Consagrou diversos sucessos como "Crazy Train",
