                </p>
                <p>
                    Ozzy formou sua primeira banda, a The Polka Tulk Blues Band, aos vinte anos de idade. Esta, que logo
                    após se chamaria apenas "Polka Tulk" e, mais tarde, "Earth". No repertório, blues e rock com
                    influências das bandas Cream, Blue Cheer e Vanilla Fudge, recebe a alcunha de "Pai do Heavy Metal",
                    sendo assim idolatrado.
                    E em 1969, após descobrir a existência de uma banda homônima, Tony Iommi (guitarra), Bill Ward
                    (bateria), Ozzy Osbourne (vocais) e Geezer Butler (baixo) decidem adotar outro nome. A ideia surgiu
                    a partir do título de uma história do escritor Dennis Wheatley (que também inspirou a composição de
                    Butler), nascendo então o Black Sabbath.
