                <h2>Pink Floyd</h2>
                <p>
                    Pink Floyd foi uma banda britânica de rock formada em Londres em 1965. Ganhando seguidores como um
                    grupo de rock psicodélico, eles se destacaram por suas composições longas, pela experimentação
                    sonora, pelas letras filosóficas e pelas apresentações ao vivo criativas, o que levou a se tornarem
                    uma banda líder do gênero do rock progressivo. Eles são um dos grupos mais bem-sucedidos
                    comercialmente e influentes da história da música popular.
                </p>
                <p>
                    O grupo foi fundado pelos estudantes Syd Barrett (guitarra, vocal), Nick Mason (bateria), Roger
                    Waters (baixo, voz) e Richard Wright (teclados, voz). Sob a liderança de Barrett, eles lançaram dois
                    singles e um álbum de estreia de sucesso, The Piper at the Gates of Dawn (1967). David Gilmour
                    entrou para a banda como guitarrista e vocalista em dezembro de 1967; Barrett saiu em abril de 1968
                    devido à deterioração de sua saúde mental. Waters se tornou o letrista principal e líder temático,
                    desenvolvendo os conceitos por trás dos álbuns The Dark Side of the Moon (1973), Wish You Were Here
                    (1975), Animals (1977), The Wall (1979) e The Final Cut (1983). A banda também compôs várias trilhas
                    sonoras para filmes.
                </p>
